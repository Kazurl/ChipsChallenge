public class Pair {

    /**
     * X-Coordinate.
     */
    int Item1;

    /**
     * Y-Coordinate.
     */
    int Item2;

    /**
     * Creates a new Pair with the specified values for the first and second integers.
     * 
     * @param f The value for the first integer (Item1).
     * @param s The value for the second integer (Item2).
     */
    public Pair(int f, int s)
    {
        this.Item1 = f;
        this.Item2 = s;
    }
}
